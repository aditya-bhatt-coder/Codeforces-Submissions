template <typename dt> void print(vector<dt> v){
    for(dt i = 0 ; i < v.size() ; i++)
        cout << v[i] << " ";
    cout << '\n';
}

void solve(){
    int n; cin >> n;
    vl a(n); input(a);

    ll gcd = 0;
    for(int i=0 ; i<n>>1 ; i++){
        ll val = abs(a[i] - a[n - i - 1]);
        if(val == 0)
            continue;
        if(i == 0)
            gcd = val;
        else
            gcd = __gcd(val, gcd);
    }

    cout << gcd << "\n";
}

